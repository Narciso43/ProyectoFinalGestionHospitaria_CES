    private void dSearchbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dSearchbtnMouseClicked
        connection = Connector.ConnectDb();
        defaultTableModel.getDataVector().removeAllElements();
        defaultTableModel.fireTableDataChanged();
        String searchKey = rSearchField.getText();
        String sql = "select count,fecha,id,nombre,edad,genero,sangre,departamento,telefono,correo,estadocivil,direccion,habitacion,usuario from medicos where nombre = '" + searchKey + "'";
        try {
            ps = connection.prepareStatement(sql);
            rs = ps.executeQuery();
            Object columnData[] = new Object[14];
            while (rs.next()) {
                columnData[0] = rs.getInt("count");
                columnData[1] = rs.getString("fecha");
                columnData[2] = rs.getString("id");
                columnData[3] = rs.getString("nombre");
                columnData[4] = rs.getInt("edad");
                columnData[5] = rs.getString("genero");
                columnData[6] = rs.getString("sangre");
                columnData[7] = rs.getString("departamento");
                columnData[8] = rs.getString("telefono");
                columnData[9] = rs.getString("correo");
                columnData[10] = rs.getString("estodocivil");
                columnData[11] = rs.getString("direccion");
                columnData[12] = rs.getInt("habitacion");
                columnData[13] = rs.getString("usuario");
                defaultTableModel.addRow(columnData);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_dSearchbtnMouseClicked

